package up.mi.projet;

/**
 * Énumération représentant les couleurs disponibles pour la coloration des sommets d'un graphe.
 * La couleur {@code AUCUNE} est utilisée par défaut pour les sommets non encore colorés.
 */
public enum Couleur {
    /**
     * Couleur par défaut pour un sommet non coloré.
     */
    AUCUNE,
    ROUGE,
    VERT,
    BLEU,
    JAUNE,
    ORANGE,
    VIOLET
}
